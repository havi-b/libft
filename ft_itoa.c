#include "libft.h"

char	*ft_itoa(int n)
{
	char	*s;
	size_t	l;

	l = ft_intlen(n);
	if (!(s = ft_strnew(l)))
		return (NULL);
	if (n == -2147483648)
		return (ft_strdup("-2147483648"));
	if (n == 0)
		*s = '0';
	if (n < 0)
	{
		s[0] = '-';
		n *= -1;
	}
	while (n > 0)
	{
		s[--l] = (n % 10) + '0';
		n /= 10;
	}
	return (s);
}
